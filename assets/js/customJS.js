$(document).ready(function(){
    $('.landing-page').hide();
    $('.generate').hide();
    $('.brief-result').show();

    $('#generate-link').on('click',function(){
        $('.landing-page').hide();
        $('.brief-result').hide();
        $('.generate').show();
    });

    $('#generate-link').on('click',function(){
        $('.landing-page').hide();
        $('.brief-result').hide();
        $('.generate').show();
    });

});